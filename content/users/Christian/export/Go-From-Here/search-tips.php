    <span style="font-variant-numeric: normal; font-variant-east-asian: normal; vertical-align: baseline; font-family: &quot;Arial Black&quot;;">
      <u>Remove term:</u> Sometimes your search results will return results that are related to your terms but they aren’t what you are looking for. For instance, you maybe searching for information about Anne Hathaway, William Shakespeare's wife who lived in the 1500’s. Odds are searching “Anne Hathaway” will return results related to the famous actress. To remove Anne Hathaway the actress from your search results you need to first, enter you search terms and then use the dash or minus sign (-) followed by terms you want to avoid.
    </span>
  </p>
  <span style="font-family: &quot;Arial Black&quot;;">
    <br>
  </span>
